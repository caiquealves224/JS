/** 
 * São Associados a essa variavel/constante metodos daquele tipo de dado 
 */
{
    const numero = 2.019;

    numero.toFixed(2);
    //2.02

    numero.toString()
    //"2.019"
}

/**
 * A Função "Number"(que se assemelha muito com uma classe/objeto) 
 * tem outros metodos dentro dele: 
 */

{
    Number.isFinite();
    //retorna true ou false

    Number.isNaN();
    //retorna true ou false

    Number.isInteger();
    //retorna true ou false
}